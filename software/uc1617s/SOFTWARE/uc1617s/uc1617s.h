/* ── 命令12: Set Partial Display Control ───────────────────────
 * 格式: 1000_01LC[10:9]   (单字节)
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 0 │ 0 │ 0 │ 0 │ 1 │10 │ 9 │  ← 0x84 | (LC10<<1) | LC9
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * LC[10:9]:
 *   0xb = 禁用部分显示, 复用率 = CEN+1 (DST、DEN 不使用)
 *   11b = 使能部分显示, 复用率 = DEN-DST+1+LC[0]×(FLT+FLB)×2
 * 默认: 00b (禁用)
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_PARTIAL              0x84
#define UC_PARTIAL_DIS              0x00    /* 禁用部分显示 */
#define UC_PARTIAL_EN               0x03    /* 使能部分显示 */


/* ── 命令27: Set COM End ───────────────────────────────────────
 * 格式: 1111_0001 + CEN[6:0]   (双字节)
 * 第1字节:
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 1 │ 1 │ 1 │ 0 │ 0 │ 0 │ 1 │  ← 0xF1 (固定)
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * 第2字节:
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ - │CN6│CN5│CN4│CN3│CN2│CN1│CN0│
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * CEN[6:0]: COM扫描结束行地址, 范围 0~127
 * CEN 定义使用的 COM 电极数量, 应与 LCD 像素行数对应
 * 实际 COM 数 = CEN + 1
 * 例: CEN=95 → COM0~COM95 (96行)
 * 默认: 127 (128行)
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_CEN                  0xF1


/* ── 命令28: Set Partial Display Start (DST) ───────────────────
 * 格式: 1111_0010 + DST[6:0]   (双字节)
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 1 │ 1 │ 1 │ 0 │ 0 │ 1 │ 0 │  ← 0xF2 (固定)
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * 第2字节 D6-D0 = DST[6:0]:
 *   局部显示起始行地址
 * 默认: 0
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_DST                  0xF2

/* ── 命令29: Set Partial Display End (DEN) ─────────────────────
 * 格式: 1111_0011 + DEN[6:0]   (双字节)
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 1 │ 1 │ 1 │ 0 │ 0 │ 1 │ 1 │  ← 0xF3 (固定)
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * 第2字节 D6-D0 = DEN[6:0]:
 *   局部显示结束行地址
 * 默认: 127
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_DEN                  0xF3

/* ── 命令30: Set Window Program Starting Page_C Address ────────
 * 格式: 1111_0100 + WPC0[4:0]  (双字节)
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 1 │ 1 │ 1 │ 0 │ 1 │ 0 │ 0 │  ← 0xF4 (固定)
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * 第2字节 D4-D0 = WPC0[4:0]:
 *   窗口编程起始页列地址
 * 默认: 0
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_WPC0                 0xF4

/* ── 命令31: Set Window Program Starting Row Address ───────────
 * 格式: 1111_0101 + WPP0[6:0]  (双字节)
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 1 │ 1 │ 1 │ 0 │ 1 │ 0 │ 1 │  ← 0xF5 (固定)
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * 第2字节 D6-D0 = WPP0[6:0]:
 *   窗口编程起始行地址
 * 默认: 0
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_WPP0                 0xF5


/* ── 命令32: Set Window Program Ending Page_C Address ──────────
 * 格式: 1111_0110 + WPC1[4:0]  (双字节)
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 1 │ 1 │ 1 │ 0 │ 1 │ 1 │ 0 │  ← 0xF6 (固定)
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * 第2字节 D4-D0 = WPC1[4:0]:
 *   窗口编程结束页列地址
 * 默认: 31
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_WPC1                 0xF6

/* ── 命令33: Set Window Program Ending Row Address ─────────────
 * 格式: 1111_0111 + WPP1[6:0]  (双字节)
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 1 │ 1 │ 1 │ 0 │ 1 │ 1 │ 1 │  ← 0xF7 (固定)
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * 第2字节 D6-D0 = WPP1[6:0]:
 *   窗口编程结束行地址
 * 默认: 127
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_WPP1                 0xF7

/* ── 命令34: Enable/Disable Window Program ─────────────────────
 * 格式: 1111_100AC[3]
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 1 │ 1 │ 1 │ 1 │ 0 │ 0 │AC3│  ← 0xF8 | AC[3]
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * AC[3]=0: 禁用窗口编程 (默认)
 * AC[3]=1: 使能窗口编程
 *
 * 注意: 更改窗口编程边界时, 必须先禁用窗口编程,
 *       然后在开始新边界编程前再重新使能。
 *
 * 使能后, CA 和 RA 的递增及环绕将自动调整,
 * 仅在窗口地址范围内进行有效的数据更新。
 *
 * 窗口编程方向由以下寄存器控制:
 *   WA (AC[0]): 到达窗口页/行边界后是否跳到下一行/页
 *   PID (AC[2]): 0=地址从 WPP0 向 WPP1 递增, 1=反向
 *   AC[1]: 0=水平递增, 1=垂直递增
 *   MX (LC[1]): 0=页地址从 WPC0 到 WPC1,
 *               1=页地址从 127-WPC0 到 127-WPC1
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_WIN_DIS              0xF8    /* 禁用窗口编程 */
#define UC_SET_WIN_EN               0xF9    /* 使能窗口编程 */



