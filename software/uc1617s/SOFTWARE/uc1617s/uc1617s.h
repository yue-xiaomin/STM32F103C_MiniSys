/* ── 命令20: Set N-Line Inversion ──────────────────────────────
 * 格式: 1100_1000 + 0100_NIV[3:0]   (双字节)
 * 第1字节:
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ 1 │ 1 │ 0 │ 0 │ 1 │ 0 │ 0 │ 0 │  ← 0xC8 (固定)
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 * 第2字节:
 * ┌───┬───┬───┬───┬───┬───┬───┬───┐
 * │ - │ - │ - │ - │IV3│IV2│IV1│IV0│
 * └───┴───┴───┴───┴───┴───┴───┴───┘
 *   NIV[1:0]: 周期选择
 *     00b = 9行    01b = 13行
 *     10b = 17行   11b = 23行
 *   NIV[2]:   0=无XOR, 1=XOR
 *   NIV[3]:   0=禁用NIV, 1=使能NIV
 * 默认: 0x06 (禁用, XOR, 17行)
 * ─────────────────────────────────────────────────────────────── */
#define UC_SET_NIV                  0xC8
#define UC_NIV_9L                   0x00    /* 9行 */
#define UC_NIV_13L                  0x01    /* 13行 */
#define UC_NIV_17L                  0x02    /* 17行 */
#define UC_NIV_23L                  0x03    /* 23行 */

#define UC_NIV_XOR                  0x04    /* XOR使能 */

#define UC_NIV_EN                   0x08    /* 使能N行反转 */

#define UC_NIV_DEFAULT              0x06    /* 默认值: 禁用+XOR+17行 */



