/*===========================================================================
 * uc1617s_font.c
 *
 * 所有字体数据预计算到 Flash (const), 零 RAM 占用
 *   5×7  → 5字节/字符, 直接存储
 *   6×8  → 6字节/字符, 5×7 + 1列空白
 *   8×8  → 8字节/字符, 1空白 + 5×7 + 2空白
 *   8×16 → 16字节/字符, 5×7 垂直×2 居中于8列
 *===========================================================================*/
#include "uc1617s_font.h"
#include "uc1617s_lcd.h"

/*===========================================================================
 * 垂直2倍放大宏 (编译期计算, 不占运行时间)
 *   DC_LO: 源 bit0~3 → 目标低字节 (行0~7)
 *   DC_HI: 源 bit4~6 → 目标高字节 (行8~15)
 *===========================================================================*/
#define DC_LO(x) (((x)&1)*0x03|((x)&2)*0x06|((x)&4)*0x0C|((x)&8)*0x18)
#define DC_HI(x) ((((x)>>4)&1)*0x03|(((x)>>5)&1)*0x0C|(((x)>>6)&1)*0x30)

/*===========================================================================
 * 字体生成宏 (编译期计算, 输出直接存入 Flash)
 *===========================================================================*/
#define F6(c0,c1,c2,c3,c4)  {c0,c1,c2,c3,c4,0x00}
#define F8(c0,c1,c2,c3,c4)  {0x00,c0,c1,c2,c3,c4,0x00,0x00}
#define F16(c0,c1,c2,c3,c4) {0x00,DC_LO(c0),DC_LO(c1),DC_LO(c2),DC_LO(c3),DC_LO(c4),0x00,0x00, \
                             0x00,DC_HI(c0),DC_HI(c1),DC_HI(c2),DC_HI(c3),DC_HI(c4),0x00,0x00}

